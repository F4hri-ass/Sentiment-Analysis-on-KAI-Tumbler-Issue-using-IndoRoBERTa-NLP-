# 1. Remove HTML tags
def remove_html(text):
    return re.sub(r'<.*?>', '', text)

# 2. Remove URLs
def remove_url(text):
    return re.sub(r'http\S+|www\.\S+', '', text)

# 3. Remove mentions (@user)
def remove_mention(text):
    return re.sub(r'@\S+', '', text)

# 4. Remove hashtag symbol but keep the word
def remove_hashtag(text):
    return re.sub(r'#(\w+)', r'\1', text)

# 5. Remove emojis
def remove_emoji(text):
    return demoji.replace(text, repl="")

# 6. Remove retweet markers (RT)
def remove_rt(text):
    return re.sub(r'\brt[\s:]+', '', text)

# 7. Remove multiple spaces
def remove_extra_spaces(text):
    return re.sub(r'\s+', ' ', text).strip()

# MASTER FUNCTION (khusus untuk BERT)
def preprocess_for_bert(text):
    text = str(text).lower()
    text = remove_html(text)
    text = remove_url(text)
    text = remove_mention(text)
    text = remove_hashtag(text)
    text = remove_emoji(text)
    text = remove_rt(text)
    text = remove_extra_spaces(text)

    return text

