from sklearn.feature_extraction.text import CountVectorizer

def top_ngram(klasifikasi, ngram_min=2, ngram_max=3, topn=20):
    text = df1[df1["label"] == klasifikasi]["clean_text"]
    vec = CountVectorizer(ngram_range=(ngram_min, ngram_max))
    ngram = vec.fit_transform(text)
    freq = ngram.sum(axis=0).A1
    vocab = {v: k for k, v in vec.vocabulary_.items()}
    top = freq.argsort()[::-1][:topn]
    return [(vocab[i], freq[i]) for i in top]

